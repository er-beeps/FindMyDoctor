package com.deerwalk.model;

public class PasswordChange {
    private String pass;
    private String name;


    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getPass() {
        return pass;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }
}
